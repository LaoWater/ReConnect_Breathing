# Oxygen (O2) - measured as a percentage (%) of the air composition, with 100% being full saturation.
# Standard example: ~ 20%

# Carbon Dioxide (CO2) - Measured in parts per million (ppm).
# Standard example: 400 ppm

# Particulate Matter (PM) - Include a variety of pollutants. Measured in micrograms per cubic meter (µg/m^3)
# Standard example: 25 µg/m^3 (micrograms per cubic meter)

# Volatile Organic Compounds (VOCs) - Measured micrograms per cubic meter (µg/m^3), sometimes ppb.
# Standard example: 7 µg/m^3 (micrograms per cubic meter)

# Ozone (O3) - Measured in micrograms per cubic meter (µg/m^3)
# Standard example: 5 µg/m^3

# Water Vapor (H2O) - Measured as Relative humidity (%)
# Standard example: 40%
